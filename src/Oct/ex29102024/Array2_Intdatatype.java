package Oct.ex29102024;

public class Array2_Intdatatype {
    public static void main(String[] args) {
        //  General Form of Java Array Initialization
        int C[] = new int[5];

        System.out.println("Length of Array C is: "+C.length);
        System.out.println("Array: ");
        //Accessing a Specific Element in a Java Array
        C[0] = 34;
        C[1] = 67;
        C[2] = 22;
        C[3] = 78;
        C[4] = 12;
        //Printing specific element in a Java Array
        System.out.println(C[0]);
        System.out.println(C[1]);
        System.out.println(C[2]);
        System.out.println(C[3]);
        System.out.println(C[4]);


    }
}
